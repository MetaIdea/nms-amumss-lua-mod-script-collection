NMS_MOD_DEFINITION_CONTAINER =
{
["MOD_FILENAME"]    = "More Storage Expansion Modules",
["MOD_AUTHOR"]      = "Reikokuu & Babscoole",
["MOD_DESCRIPTION"] = "Increases the chance and amount of multitool, ship, and freighter storage modules rewards",
["NMS_VERSION"]     = "5.73",
["MODIFICATIONS"]   =
    {
        {
            ["MBIN_CHANGE_TABLE"] =
            {
                {
                    ["MBIN_FILE_SOURCE"] = "METADATA\REALITY\TABLES\REWARDTABLE.MBIN",
                    ["MXML_CHANGE_TABLE"] =
                    {
                        {
                            ["SPECIAL_KEY_WORDS"] = {"Id", "WEAP_TOKEN", "List", "GcRewardTableItem"},
                            ["SEC_SAVE_TO"] = "ADD_NewLoot",
                        },-- RewardTable
                    }
                },
            }
        },
    }
}

local RewardTable = NMS_MOD_DEFINITION_CONTAINER["MODIFICATIONS"][1]["MBIN_CHANGE_TABLE"][1]["MXML_CHANGE_TABLE"]

for i = 1, #RewardChanges do
  local ListID = RewardChanges[i][1][1]
  local Items  = RewardChanges[i][2]

  for j = 1, #Items do
    LootID = Items[j][1]
    Min    = Items[j][2]
    Max    = Items[j][3]
    Chance = Items[j][4]

    RewardTable[#RewardTable+1] =
    {
      ["SPECIAL_KEY_WORDS"] = {"Id", ListID, "ID", LootID},
      ["SECTION_UP"] = 2,
      ["VALUE_CHANGE_TABLE"] =
      {
        {"AmountMin",        Min},
        {"AmountMax",        Max},
        {"PercentageChance", Chance},
      },
    }
  end
end

for i = 1, #AddLoot do
  local ListID = AddLoot[i][1][1]
  local Items  = AddLoot[i][2]

  for j = 1, #Items do
    LootID = Items[j][1]
    Min    = Items[j][2]
    Max    = Items[j][3]
    Chance = Items[j][4]

    RewardTable[#RewardTable+1] =
    {
      ["SEC_EDIT"] = "ADD_NewLoot",
      ["VALUE_CHANGE_TABLE"] =
      {
         {"PercentageChance", Chance},
         {"ID",               LootID},
         {"AmountMin",        Min},
         {"AmountMax",        Max},
      }
    }

    RewardTable[#RewardTable+1] =
    {
      ["SPECIAL_KEY_WORDS"] = {"Id", ListID},
      ["PRECEDING_KEY_WORDS"] = {"List", "List"},
      ["ADD_OPTION"] = "ADDafterLINE",
      ["SEC_ADD_NAMED"] = "ADD_NewLoot",
    }
  end
end