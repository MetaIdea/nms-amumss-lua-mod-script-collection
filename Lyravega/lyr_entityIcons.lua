local applyOverrides = function(entityFields, overrideValues)
	for overrideKey, overrideValue in pairs(overrideValues) do
		if fieldTypes[overrideKey] then
			local entityField = entityFields[overrideKey]

			if type(overrideValue) ~= "table" and fieldTypes[overrideKey] == type(overrideValue) then
				entityField.override = overrideValue
			else
				if overrideValue.inherit and fieldTypes[overrideValue.inherit] and fieldTypes[overrideValue.inherit] == fieldTypes[overrideKey] then
					overrideKey = overrideValue.inherit
				end

				if overrideValue.multiply and type(entityFields[overrideKey].default) == "number" then
					entityField.override = entityFields[overrideKey].default * overrideValue.multiply
				elseif overrideValue.add and type(entityFields[overrideKey].default) == "number" then
					entityField.override = entityFields[overrideKey].default * overrideValue.add
				end
			end
		end
	end
end

local searchBoth = function(searchFields, searchMbinPath, overrideValues)
	for mbinPath, entityFields in pairs(entities) do
		if string.find(mbinPath, searchMbinPath, 1, true) then
			for searchFieldName, searchFieldValue in pairs(searchFields) do
				if fieldTypes[searchFieldName] and fieldTypes[searchFieldName] == type(searchFieldValue) then
					if entityFields[searchFieldName].default == searchFieldValue
					or type(searchFieldValue) == "string"
					and string.find(searchFieldValue, "...", 1, true)
					and string.find(entityFields[searchFieldName].default, string.gsub(searchFieldValue, "%.%.%.", ""), 1, true) then
						applyOverrides(entityFields, overrideValues)
					end
				end
			end
		end
	end
end

local fieldSearch = function(searchFields, overrideValues)
	for _, entityFields in pairs(entities) do
		for searchFieldName, searchFieldValue in pairs(searchFields) do
			if fieldTypes[searchFieldName] and fieldTypes[searchFieldName] == type(searchFieldValue) then
				if entityFields[searchFieldName].default == searchFieldValue
				or type(searchFieldValue) == "string"
				and string.find(searchFieldValue, "...", 1, true)
				and string.find(entityFields[searchFieldName].default, string.gsub(searchFieldValue, "%.%.%.", ""), 1, true) then
					applyOverrides(entityFields, overrideValues)
				end
			end
		end
	end
end

local mbinPathSearch = function(searchMbinPath, overrideValues)
	for mbinPath, entityFields in pairs(entities) do
		if string.find(mbinPath, searchMbinPath, 1, true) then
			applyOverrides(entityFields, overrideValues)
		end
	end
end

local processOverride
processOverride = function(searchFields, overrideValues, searchMbinPath, searchSeparately)
	if searchSeparately and searchFields and searchMbinPath then
		fieldSearch(searchFields, overrideValues)
		mbinPathSearch(searchMbinPath, overrideValues)
	elseif searchFields and searchMbinPath then
		searchBoth(searchFields, searchMbinPath, overrideValues)
	elseif searchFields then
		fieldSearch(searchFields, overrideValues)
	elseif searchMbinPath then
		mbinPathSearch(searchMbinPath, overrideValues)
	end
end

local processEntitiesTable = function()
	local masterChangeTable = {}

	for overrideName, override in pairs(overrides) do
		if enabledOverrides[overrideName] then
			processOverride(table.unpack(override))
		end
	end

	for mbinPath, fields in pairs(entities) do
		if fields.ScanIconType ~= "" then
			local mbinChangeTable = {
				MBIN_FILE_SOURCE = mbinPath,
				EXML_CHANGE_TABLE = {
					{
						PRECEDING_KEY_WORDS = {"GcScannableComponentData.xml"},
						VALUE_CHANGE_TABLE = {}
					}
				}
			}

			for fieldName, fieldValue in pairs(fields) do
				if fieldValue.override ~= nil and fieldValue.override ~= fieldValue.default then
					local valueChangeTable = mbinChangeTable.EXML_CHANGE_TABLE[1].VALUE_CHANGE_TABLE

					valueChangeTable[#valueChangeTable+1] = {fieldName, fieldValue.override}
				end
			end

			if #mbinChangeTable.EXML_CHANGE_TABLE[1].VALUE_CHANGE_TABLE > 0 then
				masterChangeTable[#masterChangeTable+1] = mbinChangeTable
			end
		end
	end

	return masterChangeTable
end

