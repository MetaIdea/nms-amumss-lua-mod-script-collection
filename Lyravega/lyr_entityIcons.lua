		}

		Uses the "..." wildcard to allow a partial match; "ScanIconType" of the entities are searched for a partial match with "Freighter"
		Any entities with that partial "ScanIconType" match or the entities having a "ScanName" of "UI_SALVAGE_CRATE" are valid candidates
		All found entities will have their "AlwaysShowRange" set to their default value of "ScanRange" multiplied by 0.10
		In addition, their "ScanIconType" will be changed to "Scanner"; they'll show up on scanners rather than binocs for "ScanTime" seconds
		The wildcard is useful for batch processing multiple entities when their mbin path isn't a common denominator
		The value inheritance is useful for applying the changes based on another existing value

