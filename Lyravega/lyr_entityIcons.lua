		This search will find exactly one entity, and it's the cargo pods found in crashed freighters
		The reduction in "ScanRange" doesn't matter in this case, because "AlwaysShowRange" will make them easier to be found
		And there is no need to have them taggable when you can see them all the time in near proximity, like buried tech

