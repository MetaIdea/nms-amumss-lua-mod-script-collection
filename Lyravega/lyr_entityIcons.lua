		}

		A different search is done, instead of searching 'entities' through 'searchFields', 'searchMbinPath' is used
		Any entities with "/BUILDINGS/RUINS/PARTS/CRATE" in their mbin path string will be affected
		This search finds exactly two entities; the treasure chests in the ancient ruins, and the keys required to open them
		Does a similar treatment to the example above, allowing you to see them easily when in the area
		As this one does the same things as the example above, 'searchSeparately' (the last nil value) can be set to true to combine both in one

		• showDerelictStuff = 
