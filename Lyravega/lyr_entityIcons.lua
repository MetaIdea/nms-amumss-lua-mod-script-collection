--[[
	This gigantic comment block details how the above 'overrides' table works
	Below this block is the dump of all entities in the game with a 'ScannableIconComponent', prior to 4.0 version
	This file is used as a platform to quickly implement changes to the entity icons, and generate AMUMSS outputs

	• TABLE STRUCTURE / FUNCTION ARGUMENTS
	The table values and at the same time function arguments are: '{searchFields, overrideValues, searchMbinPath, searchSeparately}'
		• 'searchFields': is a table of 'fieldName', 'fieldValue' pairs
			• 'fieldName': is a field name to search for
			• 'fieldValue': is a string/boolean/number value to look for an exact match
			OR
			• 'fieldName': is a field name to search for
			• 'fieldValue': is a string value to look for a partial match
		if there are multiple pairs, then all will be considered for alterations
		partial matching on strings can be done with "..." as a wildcard
		can be used together with 'searchMbinPath'

		• 'searchMbinPath': is a partial string to search for in mbin paths
		always tries to match partially
		dangerous with least amount of common denominators
		can be used together with 'searchFields'
		
		• 'searchSeparately': is a boolean
		if there are similar changes that needs to be applied on the separate results of both the field and mbinPath searches, this can be set to true

		• 'overrideValues': is a table of 'targetFieldName', 'targetFieldChanges' pairs
			• 'targetFieldName': is the field to be altered
			• 'targetFieldChanges': is the new string/boolean/number value
			OR
			• 'targetFieldName': is the field to be altered
			• 'targetFieldChanges': is a table with some extra directives to alter the values with
				• 'inherit': is a field name to copy the default value from
				• 'multiply': is a value to multiply the default value with
				• 'add': is a value to add to the default value
		after a successful search, the 'targetFieldName' fields will be altered with the new values
		after all the overrides are processed, proper AMUMSS output will be generated

	• EXAMPLES
	Below are the explanations for some of the default overrides
		• lessFloraDistance = {
