-- ============================================================
--  Grass Draw Distance - by kuborr
--  NMS 6.24
-- ============================================================
--
--  What this does:
--    Multiplies FadeOutStartDistance and FadeOutEndDistance on
--    grass objects only. Rocks, flora, props, trees untouched.
--
--  Only grass models are targeted via SPECIAL_KEY_WORDS. 
--  It won't accidentally catch anything else
--  sharing the same biome files.
--
--  Caps are in place so nothing explodes if a biome already has
--  unusually high values (some do, found out the hard way).
--
--  File list per model was verified against REPORT.lua, each
--  model only targets the biome files it actually appears in,
--  which keeps the log clean with zero warnings.
--
--  GRASSVARIATION01 and BONEGRASS are intentionally absent.
--  They produced zero matches against 6.24 game files so they are either
--  removed by HG or renamed at some point.
-- ============================================================


-- ── Tweak these if you want a different multiplier or caps but I would not recommend that ───
local MULTIPLIER    = 2      
local MAX_CAP_LOW   = 999     
local MAX_CAP_HIGH  = 9999    
local VALUE_CEILING = 4999    
-- ─────────────────────────────────────────────────────────────


-- called for FadeOutStartDistance and FadeOutEndDistance
-- multiplies the current value then clamps it so we don't write nonsense into the files
function GrassDistMulCap(_, currentValue)
    local val = tonumber(currentValue)
    if not val then return currentValue end  -- shouldn't happen but just in case

    local result = val * MULTIPLIER

    -- tiered cap - some vanilla values are already in the hundreds so after
    -- multiplying they'd blow past 999 but not necessarily 9999
    if     result > MAX_CAP_HIGH then return tostring(MAX_CAP_HIGH)
    elseif result > MAX_CAP_LOW  then return tostring(MAX_CAP_LOW)
    end

    return tostring(result)
end

-- called for FadeOutOffsetDistance only
function GrassFixOffset(_, currentValue)
    if tonumber(currentValue) == 0 then return "10" end
    return currentValue
end


