-- build one MBIN_CHANGE_TABLE entry per model at runtime so we don't have to
-- repeat the same SECTION_UP/REPLACE_TYPE/etc block 28 times by hand
local MBIN_CT = {}
local VCT = {
    {"FadeOutStartDistance",  "GrassDistMulCap()"},
    {"FadeOutEndDistance",    "GrassDistMulCap()"},
    {"FadeOutOffsetDistance", "GrassFixOffset()"},  -- fix 0s, leave 10s alone
}
for _, entry in ipairs(GRASS_ENTRIES) do
    MBIN_CT[#MBIN_CT + 1] =
    {
        ["MBIN_FILE_SOURCE"]  = entry.files,
        ["MXML_CHANGE_TABLE"] =
        {
            {
                ["SPECIAL_KEY_WORDS"]   = {"Filename", entry.model},
                ["SECTION_UP"]          = 2,      -- go up 2 levels from the Filename line to get the object section
                ["REPLACE_TYPE"]        = "ALL",  -- hit every instance in the file, not just the first
                ["VALUE_MATCH"]         = VALUE_CEILING,
                ["VALUE_MATCH_OPTIONS"] = "<",    -- only touch values below VALUE_CEILING
                ["VALUE_CHANGE_TABLE"]  = VCT,
            },
        },
    }
end


